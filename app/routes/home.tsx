import type { Route } from "./+types/home";
import Navbar from "../../components/Navbar";
import { ArrowRight, ArrowUpRight, Clock, Layers } from "lucide-react";
import Button from "components/ui/Button";
import Upload from "components/Upload";
import { useNavigate } from "react-router";
import { createProject } from "lib/puter.action";
import { useState } from "react";

export function meta({ }: Route.MetaArgs) {
  return [
    { title: "New React Router App" },
    { name: "description", content: "Welcome to React Router!" },
  ];
}

export default function Home() {
  const navigate = useNavigate();
  const [projects, setProjects] = useState<DesignItem[]>([]);

  const handleUpload = async (base64data: string) => {
    const newId = Date.now().toString();
    const name = `Residence ${newId}`;

    const newItem = {
      id: newId,
      name,
      sourceImage: base64data,
      renderedImage: undefined,
      timestamp: Date.now(),
    }

    const saved = await createProject({ item: newItem, visibility: "private" });

    if (!saved) {
      console.error(`Failed to create project ${newId}`);
      return false;
    }

    setProjects((prev) => [...prev, saved]);
    navigate(`/visualizer/${newId}`, {
      state: {
        initialImage: saved.sourceImage,
        initialRendered: saved.renderedImage || null,
        name
      }
    });
    return true;
  }
  return (
    <div className="home">
      <Navbar />
      <section className="hero">
        <div className="announce">
          <div className="dot">
            <div className="pulse"></div>
          </div>
          <p> Introducing Roomify 2.0 - The best way to create and share your room designs</p>
        </div>
        <h1>Build beautiful spaces at the speed of thought with Roomify</h1>
        <p className="subtitle">
          Roomify is the AI-powered interior design platform that lets you create stunning room designs in seconds.
        </p>
        <div className="actions">
          <a href="#upload" className="cta">
            Start Building <ArrowRight className="icon" />
          </a>
          <Button variant="outline" size="lg" className="demo">Watch Demo</Button>
        </div>
        <div id="upload" className="upload-shell">
          <div className="grid-overlay" />
          <div className="upload-card">
            <div className="upload-head">
              <div className="upload-icon">
                <Layers className="icon" />
              </div>
              <h3>Upload Your floor plan</h3>
              <p>Supports JPG, PNG format up to 10MB</p>
            </div>
            <Upload onComplete={handleUpload} />
          </div>
        </div>
      </section>

      <section className="projects">
        <div className="section-inner">
          <div className="section-head">
            <div className="copy">
              <h2>Projects</h2>
              <p>Your latest works and shared Community projects all in one place</p>
            </div>
          </div>
          <div className="projects-grid">
            {projects.map(({ id, name, sourceImage, renderedImage, timestamp }) => (
              <div className="project-card group" key={id}>
                <div className="preview">
                  <img src={renderedImage || sourceImage} alt="Project" />
                  <div className="badge">
                    <span>Community</span>
                  </div>
                </div>
                <div className="card-body">
                  <div>
                    <h3>{name}</h3>
                    <div className="meta">
                      <Clock size={12} />
                      <span>{new Date(timestamp).toLocaleDateString()}</span>
                      <span>By Rajan</span>
                    </div>
                  </div>
                  <div className="arrow">
                    <ArrowUpRight size={18} />
                  </div>
                </div>
              </div>
            ))}
          </div>
        </div>
      </section>
    </div>
  );
}
